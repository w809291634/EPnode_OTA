  void Display_GB_12(unsigned  int x0,unsigned  int y0,unsigned int co,  char *s)
  {
    int i, j, k, x;
    char m;
    const char *pdot;
    while (*s != 0) {
      for (i=0; i<sizeof(GB_12)/sizeof(struct typFNT_GB12); i++) {
        if (GB_12[i].Index[0] == s[0] && GB_12[i].Index[1] == s[1]) {
          pdot = GB_12[i].Msk;
          for (j=0; j<24; j++) {
            m = pdot[j];
            x = j%2?4:8;
            for (k=0; k<x; k++) {
              if (m & (1<<k))Output_Pixel(x0, y0, co);
              x0 ++;   
            }
            if (x==4) {
              y0++;
              x0 -= 12;
            }
          }
          y0 -= 12;
          break;
        }
      }
      x0 += 13;
      s += 2;
    }
  }
