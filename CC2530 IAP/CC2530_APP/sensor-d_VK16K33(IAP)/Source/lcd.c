  void Display_ASCII8X16(unsigned  int x0,unsigned  int y0, unsigned int co,  char *s)
  {
    int i,j,k,x,y,xx;
    
    unsigned char qm;
    
    long int ulOffset;
    
    char  ywbuf[32]/*,temp[2]*/;
    
    for(i = 0; i<strlen((char*)s);i++)
    {
      if(((unsigned char)(*(s+i))) >= 161)
      {
        //temp[0] = *(s+i);
        //temp[1] = '\0';
        return;
      }
      
      else
      {
        qm = *(s+i);
        
        ulOffset = (long int)(qm) * 16;
        
        for (j = 0; j < 16; j ++)
        {
          ywbuf[j]=Zk_ASCII8X16[ulOffset+j];
        }
        
        for(y = 0;y < 16;y++)
        {
          for(x=0;x<8;x++) 
          {
            k=x % 8;
            
            if(ywbuf[y]&(0x80 >> k))
            {
              xx=x0+x+i*8;
              Output_Pixel(xx,y+y0, co);
            }
          }
        }
        
      }
    }     	
  }
  
  void Display_ASCII5X8(unsigned  int x0,unsigned  int y0,unsigned int co,  char *s)
  {
    unsigned char ch = *s;
    unsigned char dot;
    int i, j;
#define CWIDTH  6
    while (ch != 0) {
      if (ch<0x20 || ch > 0x7e) {
        ch = 0x20;
      }
      ch -= 0x20;
      for (i=0; i<5; i++) {
        dot = nAsciiDot5x8[ch*5+i];
        for (j=0; j<8; j++) {
          if (dot&0x80)Output_Pixel(x0+i, y0+j, co);	
          dot <<= 1;
        }
      }
      x0 += CWIDTH;
      
      ch = *++s;	
    }
  }
  
  
