  void Display_Clear(unsigned int co)
  {
    unsigned char i,j;
    write_command(0x2a);
    write_data(0x00);write_data(0x00);
    write_data(0x00);write_data(0xa0);
    write_command(0x2b);
    write_data(0x00);write_data(0x00);
    write_data(0x00);write_data(0x81);
    write_command(0x2C);
    for (i=0;i<H;i++)
      for (j=0;j<W;j++)
        LCD_DataWrite(co>>8&0xff, co&0xff);
  }
