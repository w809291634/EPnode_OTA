  void Display_ASCII6X12(unsigned  int x0,unsigned  int y0,unsigned int co,  char *s)
  {
    unsigned char ch = *s;
    unsigned char dot;
    int i, j;
#define CWIDTH  6
    while (ch != 0) {
      if (ch<0x20 || ch > 0x7e) {
        ch = 0x20;
      }
      ch -= 0x20;
      for (i=0; i<12; i++) {
        dot = nAsciiDot6x12[ch*12+i];
        for (j=0; j<6; j++) {
          if (dot&0x80)Output_Pixel(x0+j, y0+i, co);	
          dot <<= 1;
        }
      }
      x0 += CWIDTH;
      
      ch = *++s;	
    }
  }
  
