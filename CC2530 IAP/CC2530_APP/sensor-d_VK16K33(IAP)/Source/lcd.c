/*******************************************************************************
* Function Name  : SPI_LCD_Init
* Description    : Initializes the peripherals used by the SPI FLASH driver.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void SPI_LCD_Init(void)
{
#if USE_SPI
  P0SEL &= ~0x10;                                               //设置P0_4为普通IO模式
  P0DIR |= 0x10;                                                //设置P0_4为输出模式
  P0SEL &= ~0x28;                                               //设置P0_3/P0_5为外设IO模式
  
  
#else  
  P0SEL &= ~0x07;                                               //设置P0_0/P0_1/P0_2为普通IO模式
  P0DIR |= 0x07;                                                //设置P0_0/P0_1/P0_2为输出模式
#endif
}


void write_command(unsigned char c)
{
  LCD_RS = 0;
#if USE_SPI
  
#else  
  int i;
  for (i=0; i<8; i++) {
    if((0x01&(c>>(7-i))) == 0x01)
      LCD_MOSI = 1;
    else
      LCD_MOSI = 0;
    LCD_CLK = 0;
    LCD_CLK = 1;	
  }
#endif
}

void write_data(unsigned char c)
{
  LCD_RS = 1;
#if USE_SPI
  
#else  
  int i;
  for (i=0; i<8; i++) {
    if((0x01&(c>>(7-i))) == 0x01)
      LCD_MOSI = 1;
    else
      LCD_MOSI = 0;
    LCD_CLK = 0;
    LCD_CLK = 1;	
  }
#endif
}
