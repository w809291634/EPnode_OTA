#include "infrared.h"

/*********************************************************************************************
* 名称：infrared_init()
* 功能：人体红外传感器初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void infrared_init(void)
{
  P0SEL &= ~0x01;                                               //配置管脚为通用IO模式
  P0DIR &= ~0x01;                                               //配置控制管脚为输入模式
}

/*********************************************************************************************
* 名称：unsigned char get_infrared_status(void)
* 功能：获取人体红外传感器状态
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
unsigned char get_infrared_status(void)
{ 
  if(P0_0)                                                      //人体红外传感器检测引脚
    return 1;                                                   //检测到信号返回 1
  else
    return 0;                                                   //没有检测到信号返回 0
}