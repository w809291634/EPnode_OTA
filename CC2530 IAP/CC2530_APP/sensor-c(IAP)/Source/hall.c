#include "hall.h"

/*********************************************************************************************
* 名称：hall_init()
* 功能：霍尔传感器初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void hall_init(void)
{
  P0SEL &= ~0x04;                                               //配置管脚为通用IO模式
  P0DIR &= ~0x04;                                               //配置控制管脚为输入模式
}

/*********************************************************************************************
* 名称：unsigned char get_hall_status(void)
* 功能：获取霍尔传感器状态
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
unsigned char get_hall_status(void)
{ 
  if(P0_2)                                                      //霍尔传感器检测引脚
    return 0;                                                   //没有检测到信号返回 0
  else
    return 1;                                                   //检测到信号返回 1
}