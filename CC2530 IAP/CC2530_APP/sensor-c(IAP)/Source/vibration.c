#include "vibration.h"

/*********************************************************************************************
* 名称：vibration_init()
* 功能：震动传感器初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void vibration_init(void)
{
  P0SEL &= ~0x02;                                               //配置管脚为通用IO模式
  P0DIR &= ~0x02;                                               //配置控制管脚为输入模式
}

/*********************************************************************************************
* 名称：unsigned char get_vibration_status(void)
* 功能：获取震动传感器状态
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
unsigned char get_vibration_status(void)
{ 
  if(P0_1)                                                      //振动传感器检测引脚
    return 0;                                                   //没有检测到信号返回 0
  else
    return 1;                                                   //检测到信号返回 1
}