#include "beep.h"

/*********************************************************************************************
* 名称：beep_init()
* 功能：蜂鸣器传感器初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void beep_init(void)
{
  P0SEL &= ~0x08;                                               // 配置管脚为通用IO模式
  P0DIR |= 0x08;                                                // 配置控制管脚为输入模式
}
