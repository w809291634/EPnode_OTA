#include "flame.h"

/*********************************************************************************************
* 名称：flame_init()
* 功能：火焰传感器初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void flame_init(void)
{
  P0SEL &= ~0x08;                                               //配置管脚为通用IO模式
  P0DIR &= ~0x08;                                               //配置控制管脚为输入模式
}

/*********************************************************************************************
* 名称：unsigned char get_flame_status(void)
* 功能：获取火焰传感器状态
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
unsigned char get_flame_status(void)
{ 
  if(P0_3)                                                      //检测io口电平
    return 1;
  else
    return 0;
}