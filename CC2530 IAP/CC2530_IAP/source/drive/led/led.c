#include "led.h"


/*********************************************************************************************
* 名称：led_init
* 功能：led初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void led_init(void)
{
    P1SEL &= ~0x03;          //P1.0 P1.1为普通 I/O 口
    P1DIR |= 0x03;           //输出
    
    LED2 = 1;                //关LED
    LED1 = 1;
}